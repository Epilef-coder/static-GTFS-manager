  function GoogleformatState (state) {
    // Used to put a google icon for the gogle supported services.
    if (!state.id) {
      return state.text;
    }
    var $state = state.text;
    if (state.googlesupported)  {
        $state = $(
        '<span><i class="fab fa-google"></i> ' + state.id + ' - ' + state.text + '</span>'
        );
    }
    else {
        $state = $(
            '<span> ' + state.id + ' - ' + state.text + '</span>'
            );
    }
    return $state;
  };



