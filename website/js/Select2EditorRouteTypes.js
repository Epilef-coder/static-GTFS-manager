var select2RouteEditor = function(cell, onRendered, success, cancel, editorParams){

    //create input element to hold select
    var editor = document.createElement("select");
    editor.className = "form-control";
    editor.style.width = "100%";
    editor.style.height = "100%";
    onRendered(function(){
        var select_2 = $(editor);

        select_2.select2({
             placeholder: 'Select',
             data: RouteTypeList,             
             width: 300,
             minimumInputLength: 0,
             templateResult: GoogleformatState,
             theme: 'bootstrap4'
        });

        select_2.on('change', function (e) {
            success(select_2.val());
        });


        select_2.on('blur', function (e) {
            cancel();
        });
    });


    //add editor to cell
    return editor;
}